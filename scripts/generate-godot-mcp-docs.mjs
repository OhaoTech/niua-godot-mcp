#!/usr/bin/env node
import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { GODOT_MCP_TOOLS } from "../src/godot-mcp/tools/index.js";
import {
  DEFAULT_TOOL_PROFILE,
  V1_TOOL_NAMES
} from "../src/godot-mcp/server/tool-profiles.js";

const repoRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outputPath = path.join(repoRoot, "docs/godot-mcp/tools.md");
const v1Names = new Set(V1_TOOL_NAMES);

function schemaType(schema = {}) {
  if (Array.isArray(schema.type)) {
    return schema.type.join("|");
  }
  if (schema.enum) {
    return `enum(${schema.enum.join("|")})`;
  }
  return schema.type ?? "any";
}

function renderArguments(tool) {
  const properties = tool.inputSchema?.properties ?? {};
  const required = new Set(tool.inputSchema?.required ?? []);
  const entries = Object.entries(properties);
  if (entries.length === 0) {
    return "none";
  }
  return entries
    .map(([name, schema]) => `${name}${required.has(name) ? "*" : ""}:${schemaType(schema)}`)
    .join(", ");
}

function renderTool(tool) {
  const profile = v1Names.has(tool.name) ? "v1, full" : "full";
  return `| \`${tool.name}\` | ${profile} | ${escapeCell(tool.description)} | ${escapeCell(renderArguments(tool))} |`;
}

function escapeCell(value) {
  return String(value ?? "")
    .replaceAll("|", "\\|")
    .replace(/\s+/g, " ")
    .trim();
}

function renderSection(title, tools) {
  return [
    `## ${title}`,
    "",
    "| Tool | Profiles | Description | Arguments |",
    "| --- | --- | --- | --- |",
    ...tools.map(renderTool),
    ""
  ];
}

const v1Tools = GODOT_MCP_TOOLS.filter((tool) => v1Names.has(tool.name));
const fullOnlyTools = GODOT_MCP_TOOLS.filter((tool) => !v1Names.has(tool.name));
const lines = [
  "# NIUA Godot MCP Tool Catalog",
  "",
  "Generated from the manifest-backed tool catalog. Do not edit this file by hand; run `npm run godot:mcp:docs`.",
  "",
  "## Summary",
  "",
  `- Default profile: \`${DEFAULT_TOOL_PROFILE}\``,
  `- Full profile tools: ${GODOT_MCP_TOOLS.length}`,
  `- v1 profile tools: ${v1Tools.length}`,
  `- Full-only tools: ${fullOnlyTools.length}`,
  "",
  "Argument names with `*` are required by the MCP input schema.",
  "",
  ...renderSection("v1 Profile Tools", v1Tools),
  ...renderSection("Full-Only Tools", fullOnlyTools)
];

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, `${lines.join("\n").replace(/\n+$/, "")}\n`, "utf8");
process.stdout.write(`Wrote ${path.relative(repoRoot, outputPath)}\n`);
